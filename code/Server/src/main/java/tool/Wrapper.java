package tool;

public class Wrapper {
	private String first;
	private String second;
	
	public Wrapper(){}
	
	/*GET and SET*/
	public String getFirst() {
		return first;
	}
	
	public void setFirst(String s) {
		first = s;
	}
	
	public String getSecond() {
		return second;
	}
	
	public void setSecond(String s) {
		second = s;
	}
}
