package tool;

public class ByteFileWithName {
	private String fileName;
	private byte[] byteElement;
	
	public ByteFileWithName(){}
	
	public String getFileName() {
		return fileName;
	}
	
	public void setFileName(String s) {
		fileName = s;
	}
	
	public byte[] getByteElement() {
		return byteElement;
	}
	
	public void setByteElement(byte[] b) {
		byteElement = b;
	}
}
